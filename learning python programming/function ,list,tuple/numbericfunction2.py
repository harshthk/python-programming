#numberic function like modf,power,pi,degree and radian
import math
a = math.modf(45.597)#the modf function will remove point value  from  integer
print(a)
b = math.pow(2,3)# the pow function will power 2 the power of 3 which will 8
print(b)
c = math.pi#the pi function is used for the pi
print(c)
d = math.degrees(math.pi)#the degrees function is used to convert any value to degree
print(d)
e =  math.radians(180.0)#the radians function is used to convert any value to radians
print(e)